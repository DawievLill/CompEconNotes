# ╔═╡ c076a455-61b8-447a-9343-6e40995beb4c
md" ## Introduction "

# ╔═╡ d3bbec37-1160-4f40-8219-3444ce5b3ffd
md" Consider a process that has a value each time period. 

We have that $X_t$ is its value in period $t$.

We want to construct a probability model for the sequence of successive values $X_0, X_1, \ldots$. 

Simplest case is where the $X_t$ values are independent random variables. However, this is not always possible. There is likely some degree of dependence on past values. If we consider something like the process for income over time it is likely that your current income is in some way dependent on past income. 

In other words, it is reasonable to assume that the conditional distribution of $X_{t+1}$ given the entire history of the series in question $X_t, X_{t-1}, \ldots X_0$ depends on these past values only through the value at time period $t$. 

This memorylessness assumption is what defines a Markov chain. 

We reiterate, the Markov property states that the probability distribiution over $X_{t+1}$ depends only on $X_t$ and not on any other values of the process. 

A Markov chain is a type of **stochastic process**. 

**Stochastic processes** are mathematical objects that describe the evolution of random variables in time. One can write this stochastic process as, 

$$\{X_t\}_{t \in T}$$

We will work with the particular case where the stochastic process can take on a finite or countable number of possible values. 

To quickly summarise then, 

- Markov process: stochastic process with the Markov property
- Finite Markov chain: special case of the Markov process

Now let us move to a more formal description of finite Markov chains. 

